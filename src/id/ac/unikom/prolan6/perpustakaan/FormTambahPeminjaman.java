   public void countTotal(){
       
            int s = 0;
            for(int i=0;i<tableBuku.getRowCount();i++){
                String d = tableBuku.getValueAt(i, 2).toString();
                double d1 = Double.parseDouble(d);
                s+=d1;
            }
            total = s;
            totalHarga.setText("Rp. "+s);
            
   } 
