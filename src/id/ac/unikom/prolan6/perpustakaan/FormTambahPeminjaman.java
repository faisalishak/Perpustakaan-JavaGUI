     private void buatTabelDetailBuku() {
        tabelPinjam = new DefaultTableModel();
        tabelPinjam.addColumn("Kode Buku");
        tabelPinjam.addColumn("Judul");
        tabelPinjam.addColumn("Harga");
        tableBuku.setModel(tabelPinjam);
    }
    private void addBukuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBukuActionPerformed
        if (fieldIdBuku.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Anda belum memilih Buku");
        } else {
                for(int x=0;x<tableBuku.getRowCount();x++){
                    if(fieldIdBuku.getText().equals(tableBuku.getValueAt(x, 0))){
                        JOptionPane.showMessageDialog(null, "Buku yang dipinjam tidak boleh sama");
                        return;
                    }
                }
                if (tableBuku.getRowCount() == 0) {
                    buatTabelDetailBuku();
                }
                Object[] data = new Object[3];
                data[0] = fieldIdBuku.getText();
                data[1] = fieldJudul.getText();
                data[2] = harga;
                tabelPinjam.addRow(data);
                countTotal();
             
                fieldIdBuku.setText(null);
                fieldJudul.setText(null);
            
        }
    }//GEN-LAST:event_addBukuActionPerformed

