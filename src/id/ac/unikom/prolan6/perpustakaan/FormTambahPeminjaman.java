    private void simpan() {
        
       tglPinjam = tglPjm.getCalendar();
       tglKbl = tglPjm.getCalendar();
        
        // proses insert
        
        PeminjamanDAO dao = new PeminjamanDAOImpl();
        PeminjamanDetailDao dt = new PeminjamanDetailDaoImpl();
        boolean sukses = dao.pinjam(anggota.getIdAnggota(), tglPinjam,tglKbl,total);
        String idBuku;
        boolean dones = false;
        int id = dao.getIdPeminjaman();
        for(int x=0;x<tableBuku.getRowCount();x++){
            idBuku = tableBuku.getValueAt(x, 0).toString();
            dones = dt.insertDetail(id,idBuku);
         }
        if (sukses) {
            JOptionPane.showMessageDialog(this, "Peminjaman berhasil dilakukan");
            dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Peminjaman gagal dilakukan");
        }
    }
    
